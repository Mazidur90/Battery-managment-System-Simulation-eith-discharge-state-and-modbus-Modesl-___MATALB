function signals = collectSignals(truth, meas, state, diag)
%COLLECTSIGNALS Merge the important outputs from one simulation step.

signals.packVoltageV = meas.packVoltageV;
signals.packCurrentA = meas.packCurrentA;
signals.packTempC = meas.packTempC;
signals.packCoreTempC = diag.coreTempC;
signals.packSurfaceTempC = diag.surfaceTempC;
signals.socTrue = truth.trueSoc;
signals.socEst = state.est.soc;
signals.sohTrue = truth.trueSoh;
signals.sohEst = state.est.soh;
signals.minCellVoltageV = meas.minCellVoltageV;
signals.maxCellVoltageV = meas.maxCellVoltageV;
signals.contactorClosed = state.ctrl.contactorClosed;
signals.coolingOn = state.ctrl.coolingOn;
signals.heatingOn = state.ctrl.heatingOn;
signals.balanceActive = any(state.ctrl.balanceCurrentA > 0);
signals.coolingLevel = state.ctrl.coolingLevel;
signals.currentLimitA = diag.currentLimitA;
signals.derateActive = state.ctrl.derateActive;
signals.modeCode = diag.modeCode;
signals.faultLatched = diag.faultLatched;
signals.faultSeverity = diag.faultSeverity;
signals.packPowerW = state.metrics.packPowerW;
signals.lossPowerW = state.metrics.lossPowerW;
signals.remainingEnergyWh = state.metrics.remainingEnergyWh;
signals.availablePowerW = state.metrics.availablePowerW;
signals.estimatedRangeKm = state.metrics.estimatedRangeKm;
signals.dischargeEnergyWh = state.metrics.dischargeEnergyWh;
signals.throughputAh = state.metrics.throughputAh;
signals.sopDischargeW = state.est.sopDischargeW;
signals.healthScore = state.health.score;
signals.thermalPenalty = state.health.thermalPenalty;
signals.imbalancePenalty = state.health.imbalancePenalty;
signals.sensorPenalty = state.health.sensorPenalty;
signals.commId = double(state.comm.id);
signals.commModeCode = double(state.comm.modeCode);
signals.commFaultSeverity = double(state.comm.faultSeverity);
signals.commVoltageRaw = double(state.comm.voltageRaw);
signals.commCurrentRaw = double(state.comm.currentRaw);
signals.commTempRaw = double(state.comm.tempRaw);
signals.commSocRaw = double(state.comm.socRaw);
signals.commHealthRaw = double(state.comm.healthRaw);
signals.voltageResidualV = diag.voltageResidualV;
signals.tempResidualC = diag.tempResidualC;
signals.socError = diag.socError;
signals.derateMarginA = diag.derateMarginA;
signals.faults = state.supervisor.faults;
end
